export const featuredReports = reports.filter(r => r.isFeatured)

export function getReportBySlug(slug) {
  return reports.find(r => r.slug === slug)
}

export function getReportsByIndustry(industrySlug) {
  const category = industryCategories.find(c => c.slug === industrySlug)
  if (category) {
    return reports.filter(r => category.subcategories.includes(r.industry))
  }
  return reports.filter(r => r.industry === industrySlug)
}

export function getReportsByType(typeSlug) {
  return reports.filter(r => r.reportType === typeSlug)
}

export function getReportsByUseCase(useCaseSlug) {
  return reports.filter(r => r.useCases.includes(useCaseSlug))
}
